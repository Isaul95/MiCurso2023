package com.mx.proyecto.Dto;


public class Carrera {

	private Long idCarrera;
	private String nombreCarrera;
	private String identificadorSep;
	
	public Long getIdCarrera() {
		return idCarrera;
	}
	public void setIdCarrera(Long idCarrera) {
		this.idCarrera = idCarrera;
	}
	public String getNombreCarrera() {
		return nombreCarrera;
	}
	public void setNombreCarrera(String nombreCarrera) {
		this.nombreCarrera = nombreCarrera;
	}
	public String getIdentificadorSep() {
		return identificadorSep;
	}
	public void setIdentificadorSep(String identificadorSep) {
		this.identificadorSep = identificadorSep;
	}
	
	
}
